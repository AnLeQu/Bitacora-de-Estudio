/*funciones: Son líneas de código que se utilizan 
repetidas veces y para optimizar código.
En JavaScript es como una máquina pequeña 
que toma ciertas entradas (llamadas parámetros) y 
realiza una acción específica con ellas. Puedes pensar 
en una función como una receta: toma ingredientes 
(los parámetros), hace algo con ellos, y luego puede 
devolver un resultado.

function suma(a, b) 
{
  return a + b; // Esta función suma "a" y "b" y devuelve el resultado
}

Las funciones juegan un papel central en la programación 
y en el desarrollo de software, permitiendo encapsular 
bloques de código reutilizables y ejecutables. Pueden ser 
definidas para realizar tareas específicas, desde cálculos
simples hasta la manipulación compleja de datos. Las funciones
en JavaScript pueden tener o no parámetros, así como devolver 
o no valores.


Tipo de Función	
Ejemplo de Código	Uso

Sin retorno y sin parametros:
function saludo() { ... }	Ejecutando un bloque de código simples.

Sin retorno y con parametros:	
function saludar(nombre) { ... }	Ejecutando un bloque de código com argumentos.

Con retorno y sin parametros:
function generarNumeroAleatorio() { ... }	Cálculo y retorno de un valor específico.

Con retorno y con parametros:
function sumar(a, b) { ... }	Cálculo y retorno basado en argumentos.

Funcion anonima:
let saludo = function() { ... };	Definir una función sin nombre localmente.

Arrow function:
let cuadrado = x => x * x;

*/