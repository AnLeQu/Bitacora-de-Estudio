//D2-2 Verifica si un número ingresado por el usuario es positivo o negativo. Muestra una alerta informativa.
//Variable Número
let Num = 0;
//Numero Ingresado
let NumIn = prompt("Ingrese un numero acá:");
console.log(NumIn);
//Verificación
if (NumIn > Num)
    {
        alert("Este es un numero positivo");
    }
else if (NumIn < Num)
    {
        alert("Este es un número negativo");
    }
else (NumIn == Num)
    {
        alert("Este número es el cero");
    }
