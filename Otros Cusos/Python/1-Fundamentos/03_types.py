"""
Tipos de datos en Python
  Integers: Numeros enteros
  Floats: Numeros de punto flotante (decimales)
  Strings: Cadenas de texto
  Booleans: Valores booleanos (True o False)
"""
# strings
my_name = "Nicolas"
my_name = 'Santiago'
print('my_name =>', my_name)
print(type(my_name))

# int
my_age = 12
print('my_age =>', my_age)
print(type(my_age))

# boolean 
is_single = True
print('is_single =>', is_single)
print(type(is_single))

# inputs
my_age = input('¿Cuál es tu edad?')
print('my_age=>', my_age)
print(type(my_age))

